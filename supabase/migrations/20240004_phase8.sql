-- ── AI Agent Conversations ───────────────────────────────────

CREATE TABLE IF NOT EXISTS agent_conversations (
  id            uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id       uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  room_id       text        REFERENCES rooms(id) ON DELETE SET NULL,
  agent_type    text        NOT NULL
                CHECK (agent_type IN ('sketch_mentor','concept_artist','brainstorm_partner','color_expert','story_builder','design_reviewer','room_assistant')),
  title         text,
  messages      jsonb       NOT NULL DEFAULT '[]', -- [{role,content,ts}]
  context_data  jsonb       DEFAULT '{}',          -- canvas snapshot, etc.
  created_at    timestamptz NOT NULL DEFAULT now(),
  updated_at    timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS ac_user_idx  ON agent_conversations(user_id, created_at DESC);
CREATE INDEX IF NOT EXISTS ac_room_idx  ON agent_conversations(room_id);

-- Room AI notes (summaries, action items generated by room assistant)
CREATE TABLE IF NOT EXISTS room_ai_notes (
  id         uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  room_id    text        NOT NULL REFERENCES rooms(id) ON DELETE CASCADE,
  type       text        NOT NULL DEFAULT 'summary'
             CHECK (type IN ('summary','action_items','ideas','mind_map')),
  content    text        NOT NULL,
  generated_by text      DEFAULT 'claude',
  created_at timestamptz NOT NULL DEFAULT now()
);

-- ── Team Workspaces ───────────────────────────────────────────

CREATE TABLE IF NOT EXISTS workspaces (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  name         text        NOT NULL,
  slug         text        UNIQUE NOT NULL,
  description  text,
  avatar_url   text,
  owner_id     uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  plan         text        NOT NULL DEFAULT 'free' CHECK (plan IN ('free','team','enterprise')),
  member_limit integer     NOT NULL DEFAULT 5,
  created_at   timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS ws_owner_idx ON workspaces(owner_id);

CREATE TABLE IF NOT EXISTS workspace_members (
  workspace_id uuid   NOT NULL REFERENCES workspaces(id) ON DELETE CASCADE,
  user_id      uuid   NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  role         text   NOT NULL DEFAULT 'member'
               CHECK (role IN ('owner','admin','member','viewer','guest')),
  invited_by   uuid   REFERENCES profiles(id),
  joined_at    timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (workspace_id, user_id)
);

CREATE TABLE IF NOT EXISTS workspace_invites (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  workspace_id uuid        NOT NULL REFERENCES workspaces(id) ON DELETE CASCADE,
  email        text        NOT NULL,
  role         text        NOT NULL DEFAULT 'member',
  token        text        UNIQUE NOT NULL DEFAULT gen_random_uuid()::text,
  invited_by   uuid        REFERENCES profiles(id),
  expires_at   timestamptz NOT NULL DEFAULT (now() + interval '7 days'),
  accepted_at  timestamptz
);

-- ── Projects & Tasks (Kanban) ─────────────────────────────────

CREATE TABLE IF NOT EXISTS projects (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  workspace_id uuid        NOT NULL REFERENCES workspaces(id) ON DELETE CASCADE,
  name         text        NOT NULL,
  description  text,
  cover_color  text        DEFAULT '#7c5cbf',
  status       text        NOT NULL DEFAULT 'active'
               CHECK (status IN ('active','archived','completed')),
  owner_id     uuid        REFERENCES profiles(id),
  due_date     date,
  created_at   timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS proj_ws_idx ON projects(workspace_id);

CREATE TABLE IF NOT EXISTS tasks (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  project_id   uuid        NOT NULL REFERENCES projects(id) ON DELETE CASCADE,
  title        text        NOT NULL,
  description  text,
  status       text        NOT NULL DEFAULT 'todo'
               CHECK (status IN ('todo','in_progress','review','done')),
  priority     text        NOT NULL DEFAULT 'medium'
               CHECK (priority IN ('low','medium','high','urgent')),
  assignee_id  uuid        REFERENCES profiles(id),
  created_by   uuid        REFERENCES profiles(id),
  due_date     date,
  sort_order   integer     NOT NULL DEFAULT 0,
  tags         text[]      DEFAULT '{}',
  created_at   timestamptz NOT NULL DEFAULT now(),
  updated_at   timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS task_proj_idx   ON tasks(project_id, status);
CREATE INDEX IF NOT EXISTS task_assign_idx ON tasks(assignee_id);

-- ── Smart Whiteboard Elements ─────────────────────────────────

CREATE TABLE IF NOT EXISTS whiteboard_elements (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  room_id      text        NOT NULL REFERENCES rooms(id) ON DELETE CASCADE,
  type         text        NOT NULL
               CHECK (type IN ('sticky','shape','arrow','text','mindmap_node','flowchart_node','image','frame')),
  data         jsonb       NOT NULL DEFAULT '{}',
  position     jsonb       NOT NULL DEFAULT '{"x":0,"y":0}',
  size         jsonb       DEFAULT '{"w":200,"h":150}',
  style        jsonb       DEFAULT '{}',
  z_index      integer     NOT NULL DEFAULT 0,
  created_by   uuid        REFERENCES profiles(id),
  updated_by   uuid        REFERENCES profiles(id),
  locked_by    uuid        REFERENCES profiles(id),
  created_at   timestamptz NOT NULL DEFAULT now(),
  updated_at   timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS we_room_idx ON whiteboard_elements(room_id);

-- ── Presentations ─────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS presentations (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  room_id      text        NOT NULL REFERENCES rooms(id) ON DELETE CASCADE,
  presenter_id uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  title        text,
  slides       jsonb       DEFAULT '[]',   -- [{drawingId, notes}]
  is_live      boolean     NOT NULL DEFAULT false,
  viewer_count integer     NOT NULL DEFAULT 0,
  started_at   timestamptz,
  ended_at     timestamptz,
  created_at   timestamptz NOT NULL DEFAULT now()
);

