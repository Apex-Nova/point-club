-- ── Public API Keys ───────────────────────────────────────────

CREATE TABLE IF NOT EXISTS api_keys (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id      uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  name         text        NOT NULL,
  key_hash     text        UNIQUE NOT NULL,   -- SHA-256 of the actual key
  key_prefix   text        NOT NULL,          -- first 8 chars for display
  scopes       text[]      NOT NULL DEFAULT '{"read:drawings","read:profile"}',
  last_used_at timestamptz,
  expires_at   timestamptz,
  is_active    boolean     NOT NULL DEFAULT true,
  created_at   timestamptz NOT NULL DEFAULT now()
);
CREATE INDEX IF NOT EXISTS ak_user_idx ON api_keys(user_id);

CREATE TABLE IF NOT EXISTS webhook_subscriptions (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id      uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  url          text        NOT NULL,
  events       text[]      NOT NULL DEFAULT '{}',
  secret       text        NOT NULL,
  is_active    boolean     NOT NULL DEFAULT true,
  last_fired   timestamptz,
  fail_count   integer     NOT NULL DEFAULT 0,
  created_at   timestamptz NOT NULL DEFAULT now()
);

-- ── Automation Workflows ──────────────────────────────────────

CREATE TABLE IF NOT EXISTS automation_workflows (
  id           uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id      uuid        NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  workspace_id uuid        REFERENCES workspaces(id) ON DELETE CASCADE,
  name         text        NOT NULL,
  trigger_type text        NOT NULL,  -- drawing_saved | challenge_completed | battle_won | schedule
  trigger_config jsonb     DEFAULT '{}',
  actions      jsonb       NOT NULL DEFAULT '[]',  -- [{type, config}]
  is_active    boolean     NOT NULL DEFAULT true,
  run_count    integer     NOT NULL DEFAULT 0,
  last_run_at  timestamptz,
  created_at   timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS automation_runs (
  id              uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  workflow_id     uuid        NOT NULL REFERENCES automation_workflows(id) ON DELETE CASCADE,
  status          text        NOT NULL DEFAULT 'pending'
                  CHECK (status IN ('pending','running','success','failed')),
  trigger_data    jsonb       DEFAULT '{}',
  output          jsonb       DEFAULT '{}',
  error           text,
  started_at      timestamptz NOT NULL DEFAULT now(),
  completed_at    timestamptz
);

