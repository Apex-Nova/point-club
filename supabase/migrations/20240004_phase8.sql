-- AI conversations: own only
CREATE POLICY ac_select ON agent_conversations FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY ac_insert ON agent_conversations FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY ac_update ON agent_conversations FOR UPDATE USING (auth.uid() = user_id);
CREATE POLICY ac_delete ON agent_conversations FOR DELETE USING (auth.uid() = user_id);

-- Room AI notes: room members
CREATE POLICY ran_select ON room_ai_notes FOR SELECT USING (true);

-- Workspaces: members only
CREATE POLICY ws_select ON workspaces FOR SELECT USING (
  auth.uid() = owner_id OR
  EXISTS (SELECT 1 FROM workspace_members WHERE workspace_id = id AND user_id = auth.uid())
);
CREATE POLICY ws_insert ON workspaces FOR INSERT WITH CHECK (auth.uid() = owner_id);
CREATE POLICY ws_update ON workspaces FOR UPDATE USING (auth.uid() = owner_id);

CREATE POLICY wm_select ON workspace_members FOR SELECT USING (
  user_id = auth.uid() OR
  EXISTS (SELECT 1 FROM workspaces WHERE id = workspace_id AND owner_id = auth.uid())
);
CREATE POLICY wm_all ON workspace_members FOR ALL USING (
  auth.uid() = user_id OR
  EXISTS (SELECT 1 FROM workspaces WHERE id = workspace_id AND owner_id = auth.uid())
);

-- Projects + Tasks: workspace members
CREATE POLICY proj_select ON projects FOR SELECT USING (
  EXISTS (SELECT 1 FROM workspace_members WHERE workspace_id = projects.workspace_id AND user_id = auth.uid())
  OR EXISTS (SELECT 1 FROM workspaces WHERE id = projects.workspace_id AND owner_id = auth.uid())
);
CREATE POLICY proj_insert ON projects FOR INSERT WITH CHECK (
  EXISTS (SELECT 1 FROM workspace_members WHERE workspace_id = workspace_id AND user_id = auth.uid())
  OR EXISTS (SELECT 1 FROM workspaces WHERE id = workspace_id AND owner_id = auth.uid())
);
CREATE POLICY proj_update ON projects FOR UPDATE USING (auth.uid() = owner_id);

CREATE POLICY task_select ON tasks FOR SELECT USING (true);
CREATE POLICY task_all    ON tasks FOR ALL    USING (auth.uid() = created_by OR auth.uid() = assignee_id);

-- Whiteboard: public room members
CREATE POLICY we_select ON whiteboard_elements FOR SELECT USING (true);
CREATE POLICY we_all    ON whiteboard_elements FOR ALL    USING (auth.role() = 'authenticated');

-- Learning
CREATE POLICY le_all ON learning_enrollments FOR ALL USING (auth.uid() = user_id);
CREATE POLICY uc_all ON user_certifications  FOR ALL USING (auth.uid() = user_id);
CREATE POLICY uc_select ON user_certifications FOR SELECT USING (true);

-- API keys
CREATE POLICY ak_all ON api_keys FOR ALL USING (auth.uid() = user_id);
CREATE POLICY wb_all ON webhook_subscriptions FOR ALL USING (auth.uid() = user_id);

-- Automation
CREATE POLICY auto_all ON automation_workflows FOR ALL USING (auth.uid() = user_id);
CREATE POLICY run_select ON automation_runs FOR SELECT USING (
  EXISTS (SELECT 1 FROM automation_workflows WHERE id = workflow_id AND user_id = auth.uid())
);
